
'''Есть список чисел в котором необходимо удалить все числа 13.
Итоговый список вывести на экран.'''


lst = [23, 1, 45, 87, 13, 32, 98, 11, 5, 1, 90, 345, 5, 13, 33,
       56, 41, 11, 10, 12, 66, 543, 13, 83, 13, 78, 55, 22, 9]
result = []

while lst:
    value = lst.pop(0)
    if value != 13:
        result.append(value)

print(result)